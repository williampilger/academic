package exemplos;

public class Operador {

	public static void main(String[] args) {

		int a = 10;
		int b = 20;
		
		int soma = a+b;
		int sub = a-b;
		int mult = a*b;
		int div = a/b;
		
		System.out.printf(" Soma: %d \n Subtração: %d \n Multiplicação: %d \n Divisão: %d", soma, sub, mult, div);
	}

}
